<template>

    <v-card outlined>
        <v-card-title>
            SearchReport # {{$route.params.id }}
        </v-card-title>

        <v-card-text>
            <div>
                <Number label="UserId" v-model="item.userId" :editMode="editMode" @change="change" />
            </div>
            <div>
                <Number label="DeviceId" v-model="item.deviceId" :editMode="editMode" @change="change" />
            </div>
            <div>
                <String label="Status" v-model="item.status" :editMode="editMode" @change="change" />
            </div>
            <div>
                <Date label="ReportedAt" v-model="item.reportedAt" :editMode="editMode" @change="change" />
            </div>
            <div>
                <Date label="ClearedAt" v-model="item.clearedAt" :editMode="editMode" @change="change" />
            </div>
            <div>
                <String label="Contents" v-model="item.contents" :editMode="editMode" @change="change" />
            </div>
        </v-card-text>
    </v-card>

</template>

<script>
  const axios = require('axios').default;

  export default {
    name: 'SearchReportViewDetail',
    props: {
      value: Object,
    },
    data: () => ({
        item : [],
    }),
    async created() {
      var params = this.$route.params;
      var temp = await axios.get(axios.fixUrl('/searchReports/' + params.id))

      this.item = temp.data;

    },
    methods: {
    }
  }
</script>

